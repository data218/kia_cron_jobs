import pg from 'pg';
import dotenv from 'dotenv';
dotenv.config();

const client = new pg.Client({ connectionString: process.env.DATABASE_URL });
await client.connect();

try {
  console.log('Creating table public.kia_stock_report...');
  await client.query(`
    CREATE TABLE IF NOT EXISTS public.kia_stock_report (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      created_at timestamp with time zone DEFAULT now() NOT NULL,
      grn_no text,
      order_dealer text,
      facility_type text,
      kin_invoice_no text,
      kin_invoice_date text,
      sign_off_date date,
      sign_age text,
      stock_age text,
      model_code text,
      model text,
      variant_code text,
      variant text,
      color_type_code text,
      color_type text,
      exterior_color_code text,
      exterior_color_name text,
      interior_color_code text,
      interior_color_desc text,
      full_spec_code text,
      vin_number text,
      financier_name text,
      stock_location text,
      engine_no text,
      key_number text,
      order_no text,
      hsn_code text,
      excise_invoice_no text,
      kin_invoice_amt text,
      stock_status text,
      blocked text,
      remarks text,
      basic_price numeric,
      igst numeric,
      cgst numeric,
      sgst numeric,
      comp_cess numeric,
      cust_id text,
      cust_name text,
      booking_no text,
      test_drive_vin text,
      source_file text,
      source_sheet text,
      uploaded_at timestamp with time zone DEFAULT now() NOT NULL,
      row_hash text,
      full_row_hash text
    )
  `);

  console.log('Creating indexes on public.kia_stock_report...');
  await client.query(`CREATE INDEX IF NOT EXISTS idx_kia_stock_report_row_hash ON public.kia_stock_report USING btree (row_hash)`);
  await client.query(`CREATE UNIQUE INDEX IF NOT EXISTS idx_kia_stock_report_full_row_hash ON public.kia_stock_report USING btree (full_row_hash)`);
  await client.query(`CREATE UNIQUE INDEX IF NOT EXISTS kia_stock_report_vin_number_uidx ON public.kia_stock_report USING btree (vin_number) WHERE (vin_number IS NOT NULL)`);
  await client.query(`CREATE INDEX IF NOT EXISTS kia_stock_report_dealer_status_idx ON public.kia_stock_report USING btree (order_dealer, stock_status)`);
  await client.query(`CREATE INDEX IF NOT EXISTS kia_stock_report_model_age_idx ON public.kia_stock_report USING btree (model, stock_age)`);
  await client.query(`CREATE INDEX IF NOT EXISTS idx_kia_stock_report_kin_invoice_date ON public.kia_stock_report USING btree (kin_invoice_date)`);
  await client.query(`CREATE INDEX IF NOT EXISTS idx_kia_stock_report_sign_off_date ON public.kia_stock_report USING btree (sign_off_date)`);
  await client.query(`CREATE INDEX IF NOT EXISTS idx_kia_stock_report_uploaded_at ON public.kia_stock_report USING btree (uploaded_at)`);

  console.log('Copying data from kia_stock_management into kia_stock_report...');
  const res = await client.query(`
    INSERT INTO public.kia_stock_report (
      grn_no, order_dealer, facility_type, kin_invoice_no, kin_invoice_date, sign_off_date, sign_age, stock_age, model_code, model, variant_code, variant, color_type_code, color_type, exterior_color_code, exterior_color_name, interior_color_code, interior_color_desc, full_spec_code, vin_number, financier_name, stock_location, engine_no, key_number, order_no, hsn_code, excise_invoice_no, kin_invoice_amt, stock_status, blocked, remarks, basic_price, igst, cgst, sgst, comp_cess, cust_id, cust_name, booking_no, test_drive_vin, source_file, source_sheet, row_hash, full_row_hash
    )
    SELECT 
      grn_no, order_dealer, facility_type, kin_invoice_no, kin_invoice_date, sign_off_date, sign_age, stock_age, model_code, model, variant_code, variant, color_type_code, color_type, exterior_color_code, exterior_color_name, interior_color_code, interior_color_desc, full_spec_code, vin_number, financier_name, stock_location, engine_no, key_number, order_no, hsn_code, excise_invoice_no, kin_invoice_amt, stock_status, blocked, remarks, basic_price, igst, cgst, sgst, comp_cess, cust_id, cust_name, booking_no, test_drive_vin, source_file, source_sheet, row_hash, full_row_hash
    FROM public.kia_stock_management
    ON CONFLICT (full_row_hash) DO NOTHING
  `);
  console.log(`Copied ${res.rowCount} rows successfully!`);

  // Verify counts
  const countMgt = await client.query(`SELECT count(*) FROM public.kia_stock_management`);
  const countRep = await client.query(`SELECT count(*) FROM public.kia_stock_report`);
  console.log(`Verification: kia_stock_management = ${countMgt.rows[0].count} rows | kia_stock_report = ${countRep.rows[0].count} rows`);
} catch (e) {
  console.error('Error:', e);
} finally {
  await client.end();
}
